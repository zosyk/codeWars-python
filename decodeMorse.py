def decodeMorse(morse_code):
    morse_code_words = morse_code.strip().split('   ')
    words = []
    for m_word in morse_code_words:
        m_letters = m_word.split(' ')
        words += [''.join(MORSE_CODE[m_letter] for m_letter in m_letters)]

    return ' '.join(words)


